<p>Dichos datos se han extraído en su mayoría de PDFs y han sido modelados en RDF para que, aparte de ser utilizados en Mapcelona, otras aplicaciones puedan acceder a ellos sin necesidad de volver a realizar la pesada tarea de extraerlos.</p>
